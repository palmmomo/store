-- SECTION 3: แก้ไข Sequence (Fix Duplicate Key)
-- ========================================
-- รัน section นี้ถ้าพบ Error: "duplicate key value violates unique constraint"
-- (รวมเนื้อหาจาก fix_duplicate_key.sql เดิม)

-- แก้ sequence ของ materials
SELECT setval(
  pg_get_serial_sequence('materials', 'id'),
  (SELECT COALESCE(MAX(id), 0) + 1 FROM materials)
);

