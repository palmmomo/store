-- ========================================
-- SECTION 5: ตารางเพิ่มเติม (Phase 2)
-- ========================================

-- 5. ตารางสาขา (Branches) — ใช้ในใบเสนอราคา
CREATE TABLE IF NOT EXISTS branches (
    id         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name       TEXT NOT NULL,
    address    TEXT DEFAULT '',
    phone      TEXT DEFAULT '',
    tax_id     TEXT DEFAULT '',
    created_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE branches ENABLE ROW LEVEL SECURITY;

-- 6. ตารางใบเสนอราคา (Quotations)
CREATE TABLE IF NOT EXISTS quotations (
    id               INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    quotation_no     TEXT NOT NULL UNIQUE,
    branch_id        INT REFERENCES branches(id),
    customer_name    TEXT DEFAULT '',
    customer_address TEXT DEFAULT '',
    customer_tax_id  TEXT DEFAULT '',
    items            JSONB NOT NULL DEFAULT '[]',
    total_amount     FLOAT8 DEFAULT 0,
    total_in_words   TEXT DEFAULT '',
    status           TEXT DEFAULT 'draft'
                     CHECK (status IN ('draft','sent','approved')),
    created_by       UUID REFERENCES auth.users(id),
    created_at       TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_quotations_no ON quotations(quotation_no);
CREATE INDEX idx_quotations_status ON quotations(status);
ALTER TABLE quotations ENABLE ROW LEVEL SECURITY;

-- 7. ตารางการดำเนินงาน (Jobs / Kanban)
CREATE TABLE IF NOT EXISTS jobs (
    id             INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title          TEXT NOT NULL,
    description    TEXT DEFAULT '',
    status         TEXT NOT NULL DEFAULT 'รับงาน/ทำแบบ'
                   CHECK (status IN (
                     'รับงาน/ทำแบบ',
                     'เตรียมสี/วัสดุ',
                     'ลงมือสกรีน',
                     'อบแห้ง/QC',
                     'งานเสร็จพร้อมส่ง'
                   )),
    payment_status TEXT NOT NULL DEFAULT 'unpaid'
                   CHECK (payment_status IN ('unpaid','deposit','paid')),
    assigned_to    UUID REFERENCES auth.users(id),
    created_by     UUID REFERENCES auth.users(id),
    created_at     TIMESTAMPTZ DEFAULT NOW(),
    updated_at     TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_jobs_status ON jobs(status);
CREATE INDEX idx_jobs_payment ON jobs(payment_status);
ALTER TABLE jobs ENABLE ROW LEVEL SECURITY;

