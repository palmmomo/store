-- ========================================
-- SECTION 6: เพิ่ม Column ถ้าขาดในตาราง
-- ========================================
-- รันนี้ถ้าตาราง orders เดิมไม่มี payment column:
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'orders' AND column_name = 'payment'
  ) THEN
    ALTER TABLE orders ADD COLUMN payment TEXT DEFAULT 'โอนเงิน';
  END IF;
END $$;
