-- ========================================
-- SECTION 2: สร้างตารางใหม่
-- ========================================

-- 1. ตารางผู้ใช้งาน (Users)
-- เชื่อมกับ auth.users ของ Supabase
CREATE TABLE users (
    id         UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email      TEXT NOT NULL,
    role       TEXT NOT NULL DEFAULT 'technician'
               CHECK (role IN ('admin', 'accountant', 'technician')),
    created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_users_role ON users(role);
CREATE INDEX idx_users_email ON users(email);

-- 2. ตารางสินค้าคงคลัง (Stock Items)
CREATE TABLE stock_items (
    id         INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name       TEXT NOT NULL,
    unit       TEXT NOT NULL,
    quantity   FLOAT8 NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_stock_items_name ON stock_items(name);

-- 3. ตารางบันทึกการซื้อเข้า (Stock Purchases)
-- บัญชีกรอกข้อมูลการซื้อสินค้าเข้าสต็อก
CREATE TABLE stock_purchases (
    id             INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_id        INT NOT NULL REFERENCES stock_items(id) ON DELETE CASCADE,
    quantity       FLOAT8 NOT NULL,
    price_per_unit FLOAT8 NOT NULL DEFAULT 0,
    total_price    FLOAT8 NOT NULL DEFAULT 0,
    supplier       TEXT DEFAULT '',
    purchased_by   UUID REFERENCES auth.users(id),
    purchased_at   TIMESTAMPTZ DEFAULT NOW(),
    note           TEXT DEFAULT ''
);

CREATE INDEX idx_purchases_item ON stock_purchases(item_id);
CREATE INDEX idx_purchases_by ON stock_purchases(purchased_by);
CREATE INDEX idx_purchases_at ON stock_purchases(purchased_at DESC);

-- 4. ตารางบันทึกการเบิกของ (Stock Withdrawals)
-- ช่างกรอกข้อมูลการเบิกสินค้าออกจากสต็อก
CREATE TABLE stock_withdrawals (
    id            INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_id       INT NOT NULL REFERENCES stock_items(id) ON DELETE CASCADE,
    quantity      FLOAT8 NOT NULL,
    purpose       TEXT DEFAULT '',
    withdrawn_by  UUID REFERENCES auth.users(id),
    withdrawn_at  TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX idx_withdrawals_item ON stock_withdrawals(item_id);
CREATE INDEX idx_withdrawals_by ON stock_withdrawals(withdrawn_by);
CREATE INDEX idx_withdrawals_at ON stock_withdrawals(withdrawn_at DESC);


-- ========================================
-- SECTION 3: RLS (Row Level Security)
-- ========================================

ALTER TABLE users             ENABLE ROW LEVEL SECURITY;
ALTER TABLE stock_items       ENABLE ROW LEVEL SECURITY;
ALTER TABLE stock_purchases   ENABLE ROW LEVEL SECURITY;
ALTER TABLE stock_withdrawals ENABLE ROW LEVEL SECURITY;

